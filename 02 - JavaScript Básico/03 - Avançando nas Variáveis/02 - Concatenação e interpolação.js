nome = 'Erick'
idade = 22

// Resultado esperado = Erick tem 22 de idade.

// Concatenação
console.log(nome + ' tem ' + idade + ' anos de idade.')

// Interpolação
console.log(`${nome} tem ${idade} anos de idade.`)

// Tudo o que está dentro das chaves é Javascript
console.log(`${nome} tem ${idade + 5} anos de idade.`)
// Erick tem 27 anos de idade.